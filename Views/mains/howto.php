            <h1 id="1">
                1. Manual
            </h1>
            <h2 id="1.1">
                1.1 Homepage
            </h2>
            <p>
               The main page of the website is called whenever just the link of the site is called. The page changes depending on the state of the authentication. If the user is logged in he will be redirected to the homepage of the website where he is presented a list of videos. Otherwise the user is redirected to a page similar to the about page. The page can also be accsesed by clicking the homepage icon in the top right.
            </p>
            <h2 id="1.2">
                1.2 About    
            </h2>
            <p>
                The About page provides information about the website. It contains the documents that describe how to use and how it was made. It also contains the main philosophy of the website.
            </p>
            <h2 id="1.3">
                1.3 Studio
            </h2>
            <p>
                This page is where the user has his list of videos and can access them. The user also has the option of uploading a new one.
            </p>
            <h2 id="1.4">
                1.4 Create
            </h2>
            <p>
                This page is where the user can upload a new video. He has to provide a Vimeo video code, aswell as a title, description and category.
            </p>
            <h2 id="1.5">
                1.5 User
            </h2>
            <p>
                The user page provides the user with the option of changing his email or password aswell as logging out. The page also provides the ability to export all of the favourited videos of the user.
            </p>
            <h2 id="1.6">
                1.6 Notifications
            </h2>
            <p>
                The website provides the ability to see when a new video is uploaded, a new user is created or someone comments on a video. This is done through the notification menu accesed by clicking the bell icon.
            </p>
            <h2 id="1.7">
                1.7 Search
            </h2>
            <p>
                The user can search the website for videos that he wants. This can be done using the category menu which provides specific categories to search or he can write search terms and query them using the search bar. Regardless of the method he is provided a page with videos matching the terms he has searched.
            </p>
            <h2 id="1.8">
                1.8 Trending
            </h2>
            <p>
                The trending page provides the top three most viewed videos from a recent period.    
            </p>
            <h2 id="1.9">
                1.9 Login
            </h2>
            <p>
                This page authenticates the user by verifying a username and a password. If valid this will log him into the website.  
            </p>
            <h2 id="1.10">
                1.10 Signup
            </h2>
            <p>
                This page givest he user the ability to create a new DailyViR account, by providing a username, valid email, password, country aswell as gender. If vaild he is immediately authenticated into the website.    
            </p>
            <h2 id="1.11">
                1.11 View
            </h2>
            <p>
                This page displays the video associated with the page url. It also provides the title, views aswell aswell as other information about it. The page offers the ability to favourite/unfavourite a video aswell as generating a page about it's statistics. The view page also provides the ability to comment and view others' comments. The admin role also provides the ability to delete the video or delete specific comments.
            </p>
            <h2 id="1.12">
                1.12 Stats
            </h2>
            <p>
                The stats page provides visual information about the video aswell as additional information.
            </p>
